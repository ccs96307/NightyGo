text = open('Board_GUI.py', 'r', encoding='utf-8').read()


import re
result = ''
for line in text.split('\n'):
    test = re.findall('self.c\d+.setGeometry\(QtCore.QRect\(\d+, \d+, \d+, \d+\)\)', line)
    if test:
        original = test[0]
        numX = re.findall('(\(\d+,)', original)[0]
        numY = re.findall('(\(\d+, \d+,)', original)[0]

        replaceNumX = re.sub('[\(,]', '', numX)
        replaceNumX = str(int(replaceNumX)-0)
        replaceNumX = '('+replaceNumX+','
        replaceNumY = re.findall('\(\d+, (\d+),', numY)[0]
        replaceNumY = str(int(replaceNumY)-0)

        numYPart = re.findall('(\(\d+, )\d+,', numY)[0]
        replaceNumY = numYPart + replaceNumY + ','

        numY = '\\' + numY
        numX = '\\' + numX
        replaceLine = re.sub(numY, replaceNumY, original)
        replaceLine = re.sub(numX, replaceNumX, replaceLine)
        result += '        %s\n' % replaceLine
    elif re.findall('self.c\d+.setPixmap\(QtGui.QPixmap\("Image/black_stone.svg"\)\)', line):
        replace = re.sub('"Image/black_stone.svg"', '', line)
        result += '%s\n' % replace
    elif re.findall('self.c\d+ = QtWidgets.QLabel\(self.centralwidget\)', line):
        replace = re.sub('QtWidgets.QLabel', 'Chess', line)
        replace = re.sub('centralwidget', 'centralwidget, goBoard, turns', replace)
        result += '%s\n' % replace
    else:
        result += '%s\n' % line

print(result)
open('ChessSimpleFix.py', 'w', encoding='utf-8').write(result)
